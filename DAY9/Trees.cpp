// Tree Terms
// 1.Node->Basic element containing data and pointers to children
// 2. Root->Topmost node of the tree.
// 3.Parent->Node which has child nodes
// 4.Child-> Node directly connected under parent.
// 5.Edge->Connection between Parent and child
// 6. Height of tree->Number of Edges in the longest path from root to leaf
// 7.Depth of node->Number od edges from root to the node
// 8.Level->Nodes at the same depth
// 9.Subtree->Tree formed from any node.
// 10. Degree of node->Number of children of a node
// 11.Degree of Tree->Maximum degree among all nodes

